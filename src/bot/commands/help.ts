import type { CommandContext, Context } from "grammy";
import { bold } from "../format.js";

export async function helpCmd(ctx: CommandContext<Context>): Promise<void> {
  await ctx.reply(
    [
      `${bold("Phoenix Trading Bot")}`,
      ``,
      `${bold("Setup")}`,
      `/start тАФ create your trading wallet`,
      `/register [code] тАФ register your trader account with Phoenix (required)`,
      `/tos тАФ review and accept terms`,
      `/setwithdraw [pubkey] тАФ set your withdrawal address`,
      `/unlink confirm тАФ remove wallet from bot`,
      ``,
      `${bold("Wallet")}`,
      `/status тАФ show wallet, balances, settings`,
      `/balance тАФ collateral and free margin`,
      `/deposit тАФ show your deposit address`,
      `/deposit [amount] тАФ credit USDC as Phoenix collateral`,
      `/withdraw [amount] тАФ withdraw USDC to your address`,
      `/exportkey тАФ export your wallet private key (custodial escape hatch)`,
      ``,
      `${bold("Market data")}`,
      `/price [symbol] тАФ mark price + funding`,
      `/markets тАФ list available perps`,
      ``,
      `${bold("Account")}`,
      `/pos тАФ open positions`,
      `/orders тАФ open orders`,
      `/pnl [7d|30d] тАФ realized PnL`,
      `/funding тАФ recent funding payments`,
      `/history тАФ recent trades`,
      ``,
      `${bold("Trade")}`,
      `/long [symbol] [usdc] [leverage] тАФ market long`,
      `/short [symbol] [usdc] [leverage] тАФ market short`,
      `/limit [symbol] [long|short] [usdc] [price] [leverage] тАФ limit order`,
      `/close [symbol] [pct] тАФ close position`,
      `/cancel [order_id] тАФ cancel order`,
      `/cancelall [symbol] тАФ cancel all orders for a symbol`,
      ``,
      `${bold("Alerts")}`,
      `/alert [symbol] [op] [price] тАФ eg /alert SOL > 150`,
      `/alerts тАФ list active alerts`,
      `/delalert [id] тАФ delete an alert`,
      ``,
      `${bold("Settings")}`,
      `/confirm on|off тАФ toggle trade confirmations`,
      `/maxnotional [usdc] тАФ cap per-trade size`,
    ].join("\n"),
    { parse_mode: "HTML" }
  );
}
